    Skybox::Skybox(std::string skyboxPath) {
        this->directory = skyboxPath;
        this->faces.assign( {
                this->directory + "/right.jpg",
                this->directory + "/left.jpg",
                this->directory + "/top.jpg",
                this->directory + "/bottom.jpg",
                this->directory + "/front.jpg",
                this->directory + "/back.jpg"
        });
        setupSkyBox();
        loadCubemap();
    }

